# 打开名为 'sg.txt' 的文件，以只读模式打开，将文件对象赋值给变量 f
f = open('sg.txt')

# 读取文件的全部内容，并使用换行符 '\n' 对内容进行分割，得到一个包含每行内容的列表 txt0
txt0 = f.read().split('\n')

# 对 txt0 列表中的第一行内容使用空格进行分割，得到一个包含该行所有身高数据的列表 line1
line1 = txt0[0].split()
# 对 txt0 列表中的第二行内容使用空格进行分割，得到一个包含该行所有身高数据的列表 line2
line2 = txt0[1].split()
# 对 txt0 列表中的第三行内容使用空格进行分割，得到一个包含该行所有身高数据的列表 line3
line3 = txt0[2].split()

# 将 line1、line2 和 line3 三个列表合并成一个新的列表 txt，该列表包含了文件中所有的身高数据
txt = line1 + line2 + line3

# 初始化变量 s 为 0，用于累加所有身高数据的总和
s = 0
# 遍历列表 txt 中的每个元素（即每个身高数据）
for i in txt:
    # 将当前身高数据转换为整数类型，并累加到变量 s 中
    s += int(i)

# 计算平均身高，用身高总和 s 除以身高数据的数量（即列表 txt 的长度）
average = s / len(txt)

# 初始化一个空列表 d，用于存储超过平均身高的身高数据
d = []
# 再次遍历列表 txt 中的每个元素
for j in txt:
    # 将当前身高数据转换为整数类型，并与平均身高进行比较
    if int(j) > average:
        # 如果当前身高数据超过平均身高，则将其添加到列表 d 中
        d.append(j)

# 使用空格 ' ' 作为分隔符，将列表 d 中的元素连接成一个字符串 a
a = ' '.join(d)

# 输出平均身高，注意这里没有进行保留两位小数的处理，根据题目要求应该改为 print(f"{average:.2f}")
print(average)
# 输出超过平均身高的身高数据，这些数据以空格分隔
print(a)

# 关闭文件，释放系统资源，不过这里代码有误，应该是 f.close()
f.close