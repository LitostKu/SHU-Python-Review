# 从用户输入中读取两个以空格分隔的字符串，并分别赋值给变量 m 和 n
m, n = input().split()
# 将 m 和 n 从字符串类型转换为整数类型
m, n = int(m), int(n)
# 创建一个字典 s，用于将阿拉伯数字映射为中文大写数字
s = {'0': '零', '1': '壹', '2': '贰', '3': '叁', '4': '肆', '5': '伍', '6': '陆', '7': '柒', '8': '捌', '9': '玖'}
# 初始化标志变量 flag，用于记录区间内不满足条件的数的个数
flag = 0

# 使用 for 循环遍历从 m 到 n（包含 n）的所有整数
for i in range(m, n + 1):
    # 判断当前数字 i 是否能被 6 整除，因为能同时被 2 和 3 整除的数一定能被 6 整除
    if i % 6 == 0:
        # 将满足条件的整数 i 转换为字符串类型，方便后续按位处理
        a = str(i)
        # 提取百位数字对应的中文大写形式
        b = s[a[0]]
        # 提取十位数字对应的中文大写形式
        c = s[a[1]]
        # 提取个位数字对应的中文大写形式
        d = s[a[2]]

        # 判断个位和十位是否都为 0
        if a[2] == '0' and a[1] == '0':
            # 如果是，则输出百位对应的中文大写加上“佰”
            print('{}佰'.format(b))
        # 判断个位为 0 但十位不为 0
        if a[2] == '0' and a[1] != '0':
            # 输出百位、十位对应的中文大写以及“佰”和“拾”
            print('{}佰{}拾'.format(b, c))
        # 判断个位和十位都不为 0
        if a[2] != '0' and a[1] != '0':
            # 输出百位、十位、个位对应的中文大写以及“佰”、“拾”
            print('{}佰{}拾{}'.format(b, c, d))
        # 判断个位不为 0 但十位为 0
        if a[2] != '0' and a[1] == '0':
            # 输出百位、个位对应的中文大写以及“佰”和“零”
            print('{}佰零{}'.format(b, d))
    else:
        # 如果当前数字不满足能被 6 整除的条件，flag 加 1
        flag += 1

# 判断区间内不满足条件的数的个数是否等于区间长度
if flag == n - m + 1:
    # 如果相等，说明区间内没有满足条件的数，按指定格式输出提示信息
    print('[{},{}]区间内无满足要求的数'.format(m, n))