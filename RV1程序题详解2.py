# 使用eval函数将用户输入的字符串转换为字典类型，并赋值给变量dic1
# 这里用户输入的应该是形如 {1:(3,8),2:(4,9),3:(5,0),4:(1,6),5:(2,7)} 的字典字符串
dic1 = eval(input())

# 将用户输入的字符串转换为整数类型，并赋值给变量totalnum，代表公司车辆总数
totalnum = int(input())

# 初始化循环计数器i为0
i = 0

# 使用字典推导式创建字典dic2，将dic1的键值对反转，即原字典的值作为新字典的键，原字典的键作为新字典的值
# 例如，若dic1 = {1:(3,8)}，则dic2 = {(3,8):1}
dic2 = {v: r for r, v in dic1.items()}

# 初始化一个长度为5的列表s，所有元素初始值为0，用于统计每个星期几禁行车辆的数量
# 列表的索引0对应星期一，1对应星期二，以此类推
s = [0, 0, 0, 0, 0]

# 初始化一个空字典dic，用于存储最终的结果，即{星期几代码：禁行车的数量}
dic = {}

# 当i小于车辆总数totalnum时，循环执行以下操作
while i < totalnum:
    # 获取用户输入的一个车牌号，并赋值给变量num
    num = input()
    # 循环计数器i加1
    i += 1
    # 判断车牌号的最后一位是否为大写字母
    if num[-1].isupper():
        # 如果是大写字母，则将车牌号的最后一位替换为'0'
        num = num[:-1] + '0'
    # 遍历字典dic1的值（即每个星期几对应的限行尾号元组）
    for j in dic1.values():
        # 判断车牌号的最后一位（转换为整数后）是否在当前遍历的限行尾号元组中
        if int(num[-1]) in j:
            # 如果在限行尾号元组中，则获取对应的星期几代码（通过dic2字典查找）
            key = dic2[j]
            # 将对应星期几的禁行车辆数量加1
            s[key - 1] = s[key - 1] + 1
            # 将星期几代码和对应的禁行车辆数量存入字典dic中
            dic[key] = s[key - 1]

# 输出最终生成的字典dic，即{星期几代码：禁行车的数量}
print(dic)