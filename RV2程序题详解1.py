# 初始化标志变量flag为0，用于控制循环是否继续
flag = 0
# 初始化sum1为0，用于存储使用第一种优惠券商品的总金额
sum1 = 0
# 初始化sum2为0，用于存储使用第二种优惠券商品的总金额
sum2 = 0
# 当flag为0时，进入循环
while flag == 0:
    # 使用input()获取用户输入的一行数据，使用split()按空格分割成字符串列表
    # 然后使用map()函数对列表中的每个元素应用eval()函数将其转换为数值类型
    # 最后使用list()将map对象转换为列表赋值给a
    a = list(map(eval, input().split()))
    # 判断列表a的最后一个元素（即优惠券种类）是否为0
    if a[-1] == 0:
        # 如果为0，将flag设置为1，用于结束循环
        flag = 1
    # 判断列表a的最后一个元素（即优惠券种类）是否为1
    if a[-1] == 1:
        # 如果为1，将该商品的价格（a[0]）累加到sum1中
        sum1 += a[0]
    # 判断列表a的最后一个元素（即优惠券种类）是否为2
    if a[-1] == 2:
        # 如果为2，将该商品的价格（a[0]）累加到sum2中
        sum2 += a[0]

# 判断使用第一种优惠券的商品总金额sum1是否大于等于149
if sum1 >= 149:
    # 如果是，应用满减规则，从sum1中减去100
    sum1 = sum1 - 100
# 判断使用第二种优惠券的商品总金额sum2是否大于等于399
if sum2 >= 399:
    # 如果是，应用满减规则，从sum2中减去200
    sum2 = sum2 - 200

# 使用format()函数将sum1和sum2的和格式化为保留两位小数的字符串并输出
print('{:.2f}'.format(sum1 + sum2))